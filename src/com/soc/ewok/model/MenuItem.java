public class MenuItem {
	
	private String url;
	private String key;
	
		
	// Getters and Setters
	
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}
		
	
}
